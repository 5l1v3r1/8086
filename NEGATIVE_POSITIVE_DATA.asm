
DATA SEGMENT 
    
    NUM DB 00FFH
    MSG1 DB 10,13 ,'NUMBER IS +VE$'
    MSG2 DB 10,13 ,'NUMBER IS -VE$'
    
    DATA ENDS 

CODE SEGMENT 

ASSUME CS:CODE ,DS :DATA
START : 
MOV AX,DATA
MOV DS,AX

MOV AL,NUM ;STORING NUMBER IN AL (8BIT REGISTER)

ROL AL,1 ; PERFORMS THE ROTATE LEFT SIDE FOR 1 BIT 
JC CHECK 

MOV DX,OFFSET MSG1 ; POSITVE 
MOV AH,09H
INT 21H

CHECK :
MOV DX,OFFSET MSG2 ; NEGATIVE   
MOV AH,09H
INT 21H   

MOV AH,4CH
INT 21H


CODE ENDS
END START 
        